public class operations {
    //methods that perform basic math operations on 2 operands
    public float addition (float operandone, float operandtwo){
        float sum = operandone + operandtwo;
        return sum;
    }
    public float substraction (float operandone, float operandtwo){
        float substract = operandone + operandtwo;
        return substract;
    }
    public float multiplication (float operandone, float operandtwo){
        float multiply = operandone + operandtwo;
        return multiply;
    }
    public float division (float operandone, float operandtwo){
        float divide = operandone + operandtwo;
        return divide;
    }

}
